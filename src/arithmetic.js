/**
 * src/arithmetic.js
 * Functions for common math operations.
 */

/** Return the sum of two numbers. */
export function add(num1, num2) {
  return num1 + num2;
}

/** Return the difference of two numbers. */
export function subtract(num1, num2) {
  return num1 - num2;
}

/** Return the value of num1 multiplied by num2. */
export function multiply(num1, num2) {
  return num1 * num2;
}

/** Return the value of num1 divided by num2. */
export function divide(num1, num2) {
  return num1 / num2;
}

/** Return the square of num1. */
export function square(num1) {
  return num1 * num1;
}

/** Return the cube of num1. */
export function cube(num1) {
  return num1 * num1 * num1;
}

/** Return the result of raising num1 to the power of num2. */
export function power(num1, num2) {
  return num1 ** num2;
}

/** Return the remainder of num1 / num2. */
export function mod(num1, num2) {
  return num1 % num2;
}
